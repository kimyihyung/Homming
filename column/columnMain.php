		<script>
			var swiper = new Swiper(".mySwiper", {
				slidesPerView: 3,
				spaceBetween: 30,
				slidesPerGroup: 3,
				loop: true,
				loopFillGroupWithBlank: true,
				pagination: {
					el: ".swiper-pagination",
					clickable: true,
				},
				navigation: {
					nextEl: ".swiper-button-next",
					prevEl: ".swiper-button-prev",
				},
			});

			const sliderImg = document.querySelector(".slider__img2"); //보여지는 영역
			const sliderInner = document.querySelector(".slider__inner2"); //움직이는 영역
			const slider = document.querySelectorAll(".slider2"); //각각 이미지

			let currentIndex = 0; //현재 보이는 이미지
			let sliderCount = slider.length; //이미지 갯수
			let sliderWidth = sliderImg.offsetWidth; //이미지 가로 값
			let sliderClone = sliderInner.firstElementChild.cloneNode(true); //첫번째 이미지 복사
			sliderInner.appendChild(sliderClone); //첫번째 이미지를 마지막에 넣어줌

			function sliderEffect() {
				currentIndex++;
				sliderInner.style.transition = "all 0.6s";
				sliderInner.style.transform =
					"translateX(-" + sliderWidth * currentIndex + "px)";
				if (currentIndex == sliderCount) {
					setTimeout(() => {
						sliderInner.style.transition = "0s";
						sliderInner.style.transform = "translateX(0px)";
					}, 1000);
					currentIndex = 0;
				}
			}
			setInterval(sliderEffect, 5000);
		</script>
